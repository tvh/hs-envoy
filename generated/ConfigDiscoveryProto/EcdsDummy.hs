module ConfigDiscoveryProto.EcdsDummy (EcdsDummy(..)) where
import Prelude ((+), (/), (++), (.))
import qualified Prelude as Prelude'
import qualified Data.List as Prelude'
import qualified Data.Typeable as Prelude'
import qualified GHC.Generics as Prelude'
import qualified Data.Data as Prelude'
import qualified Text.ProtocolBuffers.Header as P'

data EcdsDummy = EcdsDummy{}
                 deriving (Prelude'.Show, Prelude'.Eq, Prelude'.Ord, Prelude'.Typeable, Prelude'.Data, Prelude'.Generic)

instance P'.Mergeable EcdsDummy where
  mergeAppend EcdsDummy EcdsDummy = let in EcdsDummy

instance P'.Default EcdsDummy where
  defaultValue = EcdsDummy

instance P'.Wire EcdsDummy where
  wireSize ft' self'@(EcdsDummy)
   = case ft' of
       10 -> calc'Size
       11 -> P'.prependMessageSize calc'Size
       _ -> P'.wireSizeErr ft' self'
    where
        calc'Size = 0
  wirePutWithSize ft' self'@(EcdsDummy)
   = case ft' of
       10 -> put'Fields
       11 -> put'FieldsSized
       _ -> P'.wirePutErr ft' self'
    where
        put'Fields = P'.sequencePutWithSize []
        put'FieldsSized
         = let size' = Prelude'.fst (P'.runPutM put'Fields)
               put'Size
                = do
                    P'.putSize size'
                    Prelude'.return (P'.size'WireSize size')
            in P'.sequencePutWithSize [put'Size, put'Fields]
  wireGet ft'
   = case ft' of
       10 -> P'.getBareMessageWith (P'.catch'Unknown' P'.discardUnknown update'Self)
       11 -> P'.getMessageWith (P'.catch'Unknown' P'.discardUnknown update'Self)
       _ -> P'.wireGetErr ft'
    where
        update'Self wire'Tag old'Self
         = case wire'Tag of
             _ -> let (field'Number, wire'Type) = P'.splitWireTag wire'Tag in P'.unknown field'Number wire'Type old'Self

instance P'.MessageAPI msg' (msg' -> EcdsDummy) EcdsDummy where
  getVal m' f' = f' m'

instance P'.GPB EcdsDummy

instance P'.ReflectDescriptor EcdsDummy where
  getMessageInfo _ = P'.GetMessageInfo (P'.fromDistinctAscList []) (P'.fromDistinctAscList [])
  reflectDescriptorInfo _
   = Prelude'.read
      "DescriptorInfo {descName = ProtoName {protobufName = FIName \".envoy.service.extension.v3.EcdsDummy\", haskellPrefix = [], parentModule = [MName \"ConfigDiscoveryProto\"], baseName = MName \"EcdsDummy\"}, descFilePath = [\"ConfigDiscoveryProto\",\"EcdsDummy.hs\"], isGroup = False, fields = fromList [], descOneofs = fromList [], keys = fromList [], extRanges = [], knownKeys = fromList [], storeUnknown = False, lazyFields = False, makeLenses = False, jsonInstances = False, mapEntry = False}"

instance P'.TextType EcdsDummy where
  tellT = P'.tellSubMessage
  getT = P'.getSubMessage

instance P'.TextMsg EcdsDummy where
  textPut msg = Prelude'.return ()
  textGet = Prelude'.return P'.defaultValue
    where